@with_db_session
def ajouter_plante(
    nom: str,
    type_plante: str,
    emplacement: str,
    date_plantation: date = None,
    date_recolte: date = None,
    notes: str = "",
    db=None
) -> bool:
    """Ajoute une nouvelle plante au jardin"""
    try:
        item = GardenItem(
            nom=nom,
            type=type_plante,
            location=emplacement,
            date_plantation=date_plantation or date.today(),
            date_recolte_prevue=date_recolte,
            notes=notes,
            statut="actif"
        )
        db.add(item)
        db.commit()
        clear_maison_cache()
        return True
    except Exception as e:
        st.error(f"‚ùå Erreur ajout plante: {e}")
        return False


